function setLanguage(lang) {
    localStorage.setItem('mrc_lang', lang);
    applyTranslations(lang);
    updateToggleUI(lang);
}

function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = translations[lang][key];
        
        if (translation) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                // If it contains an icon (<i>), preserve it
                const icon = el.querySelector('i');
                if (icon) {
                    el.innerHTML = '';
                    el.appendChild(icon);
                    el.appendChild(document.createTextNode(' ' + translation));
                } else {
                    el.textContent = translation;
                }
            }
        }
    });

    // Update Page Title if exists
    if (translations[lang].booking_title && document.title.includes('Book Your Trip')) {
        document.title = translations[lang].booking_title;
    }
}

function updateToggleUI(lang) {
    const buttons = document.querySelectorAll('.lang-btn');
    buttons.forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('mrc_lang') || 'en';
    applyTranslations(savedLang);
    updateToggleUI(savedLang);
});
